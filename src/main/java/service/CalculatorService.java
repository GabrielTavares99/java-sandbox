package service;

public class CalculatorService {

    public double sum(double num1, double num2){
        return num1 + num2;
    }

    public double minus(double num1, double num2){
        return num1 - num2;
    }

    public int divide(int num1, int num2){
        return num1 / num2;
    }

    public double multiple(double num1, double num2){
        return num1 * num2;
    }

}
